<template>
  <div>
    <Banner
      :message="message"
      status="success"
    />
    <form @submit.prevent="checkForm">
      <div class="moj-page-header-actions govuk-!-margin-bottom-2">
        <div class="moj-page-header-actions__title">
          <h1 class="govuk-heading-l">
            Review ({{ totalApplicationRecords }})
          </h1>
        </div>
        <div
          v-if="hasPermissions([PERMISSIONS.applicationRecords.permissions.canUpdateApplicationRecords.value])"
          class="moj-page-header-actions__actions float-right"
        >
          <div class="moj-button-menu">
            <div class="moj-button-menu__wrapper">
              <button
                class="govuk-button moj-button-menu__item moj-page-header-actions__action govuk-!-margin-right-2"
                :disabled="isButtonDisabled"
              >
                Set status
              </button>
            </div>
          </div>
        </div>
      </div>
      <p
        v-if="!applicationRecords.length"
        class="govuk-body"
      >
        No Applications
      </p>
      <Table
        v-model:selection="selectedItems"
        data-key="id"
        :data="applicationRecords"
        :columns="tableColumns"
        multi-select
        :page-size="50"
        :custom-search="{
          placeholder: 'Search candidate names',
          handler: candidateSearch,
          field: 'candidate.id',
        }"
        :filters="[
          {
            title: 'Status',
            field: 'status',
            type: 'checkbox',
            options: availableStatuses.concat(['']),
          },
        ]"
        @change="getTableData"
      >
        <template #row="{row}">
          <TableCell :title="tableColumns[0].title">
            <RouterLink
              :to="{ name: 'exercise-application', params: { applicationId: row.id } }"
            >
              {{ row.application.referenceNumber }}
            </RouterLink>
          </TableCell>
          <TableCell :title="tableColumns[1].title">
            <RouterLink
              :to="{ name: 'candidates-view', params: { id: row.candidate.id } }"
              target="_blank"
            >
              {{ row.candidate.fullName }}
            </RouterLink>
          </TableCell>
          <TableCell :title="tableColumns[2].title">
            {{ $filters.candidateHasIssues(row) }}
          </TableCell>
          <TableCell :title="tableColumns[3].title">
            {{ $filters.lookup(row.status) }}
          </TableCell>
          <TableCell :title="tableColumns[4].title">
            {{ $filters.toYesNo(row.flags.empApplied) }}
          </TableCell>
        </template>
      </Table>
    </form>
  </div>
</template>

<script>
import Banner from '@jac-uk/jac-kit/draftComponents/Banner.vue';
import Table from '@jac-uk/jac-kit/components/Table/Table.vue';
import TableCell from '@jac-uk/jac-kit/components/Table/TableCell.vue';
import permissionMixin from '@/permissionMixin';

export default {
  name: 'ReviewList',
  components: {
    Banner,
    Table,
    TableCell,
  },
  mixins: [permissionMixin],
  data() {
    return {
      message: null,
      selectedItems: [],
      tableColumns: [
        { title: 'Reference number' },
        { title: 'Name', sort: 'candidate.fullName', default: true },
        { title: 'Issues' },
        { title: 'Status' },
        { title: 'EMP' },
      ],
    };
  },
  computed: {
    applicationRecords() {
      return this.$store.state.stageReview.records;
    },
    totalApplicationRecords() {
      return (this.exercise && this.exercise._applicationRecords && this.exercise._applicationRecords.review) || 0;
    },
    exercise() {
      return this.$store.state.exerciseDocument.record;
    },
    isButtonDisabled() {
      const isDisabled = this.selectedItems && this.selectedItems.length;
      return !isDisabled;
    },
    availableStatuses() {
      const shortlistingMethods = this.exercise.shortlistingMethods;
      const otherShortlistingMethod = this.exercise.otherShortlistingMethod || [];
      return this.$store.getters['stageReview/availableStatuses'](shortlistingMethods, otherShortlistingMethod) ;
    },
  },
  async created() {
    this.message = await this.$store.dispatch('stageReview/getMessages');
    this.selectedItems = this.$store.state.stageReview.selectedItems;
  },
  methods: {
    checkForm() {
      this.$store.dispatch('stageReview/storeItems', { items: this.selectedItems });
      this.$router.push({ name: 'exercise-stages-review-edit' });
    },
    getTableData(params) {
      this.$store.dispatch(
        'stageReview/bind',
        {
          exerciseId: this.exercise.id,
          ...params,
        }
      );
    },
    async candidateSearch(searchTerm) {
      return await this.$store.dispatch('candidates/search', { searchTerm: searchTerm, exerciseId: this.exercise.id });
    },
  },
};
</script>
