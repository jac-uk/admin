<template>
  <div>
    <div class="govuk-grid-row print-none">
      <div class="govuk-grid-column-full">
        <div class="text-right">
          <button
            v-if="isFavourites"
            class="govuk-button govuk-button--secondary govuk-!-margin-right-3 govuk-!-margin-bottom-0"
            @click="showAll"
          >
            View live exercises
          </button>
          <button
            v-else
            class="govuk-button govuk-button--secondary govuk-!-margin-right-3 govuk-!-margin-bottom-0"
            @click="showMyFavourites"
          >
            Show my favourites
          </button>
          <button
            v-if="isArchived"
            class="govuk-button govuk-button--secondary govuk-!-margin-right-3 govuk-!-margin-bottom-0"
            @click="showAll"
          >
            View live exercises
          </button>
          <button
            v-else
            class="govuk-button govuk-button--secondary govuk-!-margin-right-3 govuk-!-margin-bottom-0"
            @click="showArchived"
          >
            View archived exercises
          </button>
          <br>
          <!-- <Select hint="label">
            <option>
              bb
            </option>
          </Select> -->
          <router-link
            v-if="hasPermissions([PERMISSIONS.exercises.permissions.canCreateExercises.value])"
            ref="linkToNewExercise"
            to="/create-exercise"
            class="govuk-button govuk-!-margin-3 govuk-!-margin-bottom-0"
          >
            Create an exercise
          </router-link>
        </div>
      </div>
    </div>
    <div class="govuk-grid-row">
      <div class="govuk-grid-column-full">
        <h1
          v-if="isFavourites"
          class="govuk-heading-xl"
        >
          Favourite exercises
        </h1>
        <h1
          v-else-if="isArchived"
          class="govuk-heading-xl"
        >
          Archived exercises
        </h1>
        <h1
          v-else
          class="govuk-heading-xl"
        >
          Live exercises
        </h1>
        <form
          class="exercises-table"
          @submit.prevent="checkForm"
        >
          <Table
            ref="exercisesTable"
            data-key="id"
            :data="tableData"
            :page-size="50"
            :columns="tableColumns"
            :filters="[
              {
                type: 'dateRange',
                field: 'applicationOpenDate',
                title: 'Open date',
              },
            ]"
            :search="['name']"
            multi-select
            :selection.sync="selectedItems"
            @change="getTableData"
          >
            <template #actions>
              <button
                class="govuk-button moj-button-menu__item moj-page-header-actions__action govuk-!-margin-right-2 govuk-!-margin-bottom-3"
                :disabled="isButtonDisabled"
              >
                Export data
              </button>
            </template>
            <template #row="{row}">
              <TableCell :title="tableColumns[0].title">
                <RouterLink
                  :to="{ name: 'exercise-overview', params: { id: row.id } }"
                >
                  {{ row.referenceNumber }}
                </RouterLink>
              </TableCell>
              <TableCell :title="tableColumns[1].title">
                <RouterLink
                  :to="{ name: 'exercise-overview', params: { id: row.id } }"
                >
                  {{ row.name }}
                </RouterLink>
              </TableCell>
              <TableCell :title="tableColumns[2].title">
                <RouterLink
                  :to="{ name: 'exercise-overview', params: { id: row.id } }"
                >
                  {{ row.applicationOpenDate | formatDate }}
                </RouterLink>
              </TableCell>
              <TableCell :title="tableColumns[3].title">
                <RouterLink
                  :to="{ name: 'exercise-overview', params: { id: row.id } }"
                >
                  {{ row.applicationCloseDate | formatDate }}
                </RouterLink>
              </TableCell>
              <TableCell :title="tableColumns[4].title">
                {{ getExerciseStatus(row) }}
              </TableCell>
              <TableCell
                class="govuk-table__cell--numeric"
                :title="tableColumns[5].title"
              >
                {{ row.applicationsCount | formatNumber }}
              </TableCell>
            </template>
          </Table>
        </form>
      </div>
    </div>
  </div>
</template>

<script>
import { mapState } from 'vuex';
import Table from '@jac-uk/jac-kit/components/Table/Table';
import TableCell from '@jac-uk/jac-kit/components/Table/TableCell';
import permissionMixin from '@/permissionMixin';

export default {
  name: 'Exercises',
  components: {
    Table,
    TableCell,
  },
  mixins: [permissionMixin],
  data() {
    return {
      selectedItems: [],
      tableColumns: [
        { title: 'Reference number', sort: 'referenceNumber', direction: 'desc', default: true },
        { title: 'Name', sort: 'name' },
        { title: 'Open date', sort: 'applicationOpenDate' },
        { title: 'Close date', sort: 'applicationCloseDate' },
        { title: 'Status' },
        {
          title: 'Applications count',
          sort: '_applications._total',
          class: 'govuk-table__header--numeric',
        },
      ],
    };
  },
  computed: {
    ...mapState('exerciseCollection', [
      'records',
      'isFavourites',
      'isArchived',
    ]),
    isButtonDisabled() {
      const hasSelection = this.selectedItems && this.selectedItems.length;
      return !hasSelection;
    },
    tableData() {
      return this.records.map(row => {
        const data = { ...row };
        data.id = row.id;
        data.applicationsCount = (row._applications && row._applications._total) || 0;
        return data;
      });
    },
  },
  watch: {
    isFavourites() {
      if (this.$refs['exercisesTable']) {
        this.$refs['exercisesTable'].reload();
      }
    },
    isArchived() {
      if (this.$refs['exercisesTable']) {
        this.$refs['exercisesTable'].reload();
      }
    },
  },
  destroyed() {
    this.$store.dispatch('exerciseCollection/unbind');
  },
  methods: {
    showMyFavourites() {
      this.$store.dispatch('exerciseCollection/showFavourites');
    },
    showAll() {
      this.$store.dispatch('exerciseCollection/showAll');
    },
    showArchived() {
      this.$store.dispatch('exerciseCollection/showArchived');
    },
    getTableData(params) {
      this.$store.dispatch(
        'exerciseCollection/bind',
        params
      );
    },
    checkForm() {
      this.$store.dispatch('exerciseCollection/storeItems', { items: this.selectedItems });
      this.$router.push({ name: 'exercises-export' });
    },
    getExerciseStatus(exercise) {
      let status = '';
       
      if (exercise.state === 'archived') {
        status += 'Archived';
      } else if (exercise.state === 'draft') {
        status += 'Draft';
      } else {
        status += 'Live';
      }

      status += exercise.published ? ' (Published)' : ' (Unpublished)';

      return status;
    },
  },
};
</script>

